num_1 = 13
num_2 = 7
# sum :
num_3 = num_1 + num_2
print('num_3:', num_3)
# unary operators :
num_4 = -num_3
print('num_4:', num_4)
# binary operators :
num_5 = num_3 * -1
print('num_5:', num_5)
# division float :
num_6 = num_1 / num_2
print('num_6:', num_6)
# division floor :
num_7 = num_1 // num_2
print('num_7:', num_7)
# subtract :
num_8 = num_6 - num_7
print('num_8:', num_8)
# modular , remainder :
num_9 = num_1 % num_2
print('num_9:', num_9)
# multipication :
num_10 = num_1 * num_2
print('num_10:', num_10)
# exponents :
num_11 = num_1 ** num_2
print('num_11:', num_11)
